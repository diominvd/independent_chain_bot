def language(language_code: str) -> str:
    if language_code not in ["ru", "en"]:
        return "en"
    else:
        return language_code


def inviter(message: Message) -> int | None:
    text: list = message.text.split(" ")
    if len(text) == 2:
        inviter_id: int = int(text[1])
        return inviter_id
    else:
        return None


